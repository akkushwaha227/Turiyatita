public class Second_carousels_Helper {

    private int circleImage;
    private String text;

    public Second_carousels_Helper(int circleImage, String text) {
        this.circleImage = circleImage;
        this.text = text;
    }

    public int getCircleImage() {
        return circleImage;
    }

    public String getText() {
        return text;
    }
}
