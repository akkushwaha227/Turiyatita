package com.example.turiyatita.Carousels;

import android.widget.TextView;

public class Fifth_carousels_Helper {
    private int image;
    private String textView;

    public Fifth_carousels_Helper(int image, String textView) {
        this.image = image;
        this.textView = textView;
    }

    public int getImage() {
        return image;
    }

    public String getTextView() {
        return textView;
    }
}
